package com.mkp.dp.creational;

public class DiffAbstractAndFactoryMethodPattern {
/**
 * | Factory Method Pattern                             | Abstract Factory Pattern                                                           |
| -------------------------------------------------- | ---------------------------------------------------------------------------------- |
| Creates one product at a time                      | Creates families of related or dependent products                                  |
| Lower level; defines a single factory method       | Higher level; creates multiple factory methods grouped as one factory              |
| Relies on inheritance (subclasses override method) | Uses composition; encapsulates multiple factory methods in an object               |
| Suitable for single product creation               | More flexible; ideal for related product families                                  |
| Creating different types of vehicles               | Creating UI components for different operating systems (buttons, checkboxes, etc.) |
| Focused on how a single product is created         | Focused on creating a set of products that work together                           |
 */
}
